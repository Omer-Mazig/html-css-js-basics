// Arithmetic Operators:
let x = 5;
let y = 2;
console.log('x + y =', x + y); // Addition
console.log('x - y =', x - y); // Subtraction
console.log('x * y =', x * y); // Multiplication
console.log('x / y =', x / y); // Division

// Assignment Operators:
let a = 10;
let b = 5;
a += b; // a = a + b
console.log('a =', a);

// Comparison Operators:
let c = 10;
let d = 5;
console.log('c === d is', c === d); // Equal to
console.log('c !== d is', c !== d); // Not equal to

// Logical Operators:
let e = 10;
let f = 5;
console.log('e > 5 && f < 10 is', e > 5 && f < 0); // Logical and
console.log('e > 5 || f < 10 is', e > 5 || f < 0); // Logical or